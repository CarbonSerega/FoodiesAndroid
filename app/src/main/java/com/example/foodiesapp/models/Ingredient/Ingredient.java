package com.example.foodiesapp.models.Ingredient;

public final class Ingredient {
    private int id;
    private String name;
    private float amount;
    private String unit;


    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public float getAmount() {
        return amount;
    }

    public String getUnit() {
        return unit;
    }
}
